#' Subtract Function
#'
#' This function is good to subtract
#'
#' @param a A any number
#' @param b B is another random number
#'
#' @examples
#' subtrair(5,9)
#'
#' @export
subtrair <- function(a, b = 2) {
  a - b
}

#' Sum Function
